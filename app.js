const login = document.querySelector(".login_btn") 
const popup = document.querySelector(".popup") 
const main = document.querySelector(".none") 
const name__value = document.querySelector(".name") 
const pass__value = document.querySelector(".pass") 
const btn = document.querySelector(".signBtn") 
const change = document.querySelector(".change") 
const change_txt = document.querySelector(".change_text") 
const SignUp2 = document.querySelector(".signBtn") 
 
let nameValue = name__value.value 
let passValue = pass__value.value 
 
login.addEventListener("click", ()=> { 
    popup.style.display = "block" 
    popup.style.transform = "translateY(50px)" 
    main.style.display = "none" 
    document.body.style.height = "auto" 
    btn.addEventListener("click", ()=> { 
        localStorage.setItem("name", name__value.value) 
        localStorage.setItem("password", pass__value.value) 
        popup.style.display = "none" 
        popup.style.transform = "translateY(-600px)" 
        main.style.display = "block" 
        change_txt.innerHTML = name__value.value 
        change.innerHTML = "SIGN OUT"
        SignUp2.addEventListener("click", ()=> {
            main.style.background = "transparent"
        })
        change.addEventListener("click", ()=> { 
            localStorage.removeItem("name", name__value.value) 
            localStorage.removeItem("password", pass__value.value) 
            change.innerHTML = "LOGIN" 
            change_txt.style.display = "none"
            main.style.display = "none"
            document.body.style.background = "black"
           
        }) 
    }) 
})




